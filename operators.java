public class operators
{
    public static void main(String[] args)
    {
        //ARITHMETIC OPERATOR
        int x=20,y=10;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(++y);
        System.out.println(y++);
        x+=10;
        System.out.println(x);
        x-=10;
        System.out.println(x);
        x*=10;
        System.out.println(x);
        x/=10;
        System.out.println(x);
        x%=10;
        System.out.println(x);
        x&=10;
        System.out.println(x);
        x|=10;
        System.out.println(x);
        x^=10;
        System.out.println(x);
        x>>=10;
        System.out.println(x);
        x<<=10;
        System.out.println(x);
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x>=y);
        System.out.println(x<=y);
        System.out.println(x<10 && x>100);
        System.out.println(x<10 || x>100);
        System.out.println(!(x<10 && x>100));



        
                


    }
}